/* Flags used by string_print_va. */

/* Pad with zero */

#define STRING_ZERO_PAD        1

/* Unsigned/signed long. */

#define STRING_SIGN            2

/* Show plus. */

#define STRING_PLUS            4

/* Space if plus. */

#define STRING_SPACE           8

/* Left justified */

#define STRING_LEFT            16

/* 0x. */

#define STRING_SPECIAL         32

/* Use 'ABCDEF' instead of 'abcdef'. */

#define STRING_LARGE           64
